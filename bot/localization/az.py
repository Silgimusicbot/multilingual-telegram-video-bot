# File size formatting
def format_size(bytes_size):
    """Format file size in Azerbaijani"""
    if bytes_size is None:
        return "bilinmir"
    
    for unit in ['bayt', 'KB', 'MB', 'GB']:
        if bytes_size < 1024.0:
            return f"{bytes_size:.1f} {unit}"
        bytes_size /= 1024.0
    return f"{bytes_size:.1f} TB"

